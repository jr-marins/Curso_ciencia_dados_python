# O python é case sensitive, ou seja ele tem diferenciação entre letra maiusculas e minusculas.
frutas = ['laranja', 'uva', 'limao']
          
print("laranja" in frutas)#laranja esta na lista frutas ?
print("melão" not in frutas)#melão nao esta na lista frutas ?

# Aqui estou usando o operador logico 'in' para comparar se algum ojeto está presente na minha lista. Em python declaramos lista com as [].